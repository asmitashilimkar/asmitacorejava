package com.wrapper.classes;

public class MyInteger {
	
	public Integer boxing(int no) {
		return no;
	}
	
	public Integer unboxing(int no) {
		return no;
	}
	
	public Integer StringToNum(String no1) {
		return Integer.parseInt(no1);
	}
	
	
	public Float boxing(float no) {
		return no;
	}
	
	public Float unboxing(float no) {
		return no;
	}
	
	public Float StringToNum1(String no1) {
		return Float.parseFloat(no1);
	}
	
	

}
